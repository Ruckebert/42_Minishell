#include "libft.h"

int	ft_isalpha(int chara)
{
	if ((chara >= 'a') && (chara <= 'z'))
	{
		return (1);
	}
	else if ((chara >= 'A') && (chara <= 'Z'))
	{
		return (1);
	}
	else
		return (0);
}
